def global_alignment(seq1, seq2, n, m):

    # Initialize zero matrix
    path_grid = np.zeros((n+1, m+1), dtype=object)
    path_grid[0, 0] = 'none'

    # Fill row and column
    for i in range(n):
        path_grid[i+1, 0] = 'down '
    for j in range(m):  
        path_grid[0, j+1] = 'right'

    grid = np.zeros((n+1, m+1), dtype=int)

    # Fill 
    # Fill row and column
    for i in range(n):
        grid[i+1, 0] = -(i+1) * 5
    for j in range(m):
        grid[0, j+1] = -(j+1) * 5

    for i in range(n):
        for j in range(m):
            seq1_base = seq1[i]
            seq2_base = seq2[j]
            penalty = blosum62[seq1_base][seq2_base]

            x = grid[i, j+1] - 5
            y = grid[i+1, j] - 5
            z = grid[i, j] + penalty
            maxyz = max(x, y, z)
            if maxyz == x:
                path_grid[i+1, j+1] = 'down '
            elif maxyz == y:
                path_grid[i+1, j+1] = 'right'
            else:
                path_grid[i+1, j+1] = 'diag '
            grid[i+1, j+1] = maxyz

    # Longest path
    p = n
    q = m
    longest_path_seq1 = []
    longest_path_seq2 = []
    while p >= 0 and q >= 0:
        direction = path_grid[p, q]
        if direction == 'down ':
            p -= 1
            longest_path_seq1.append(seq1[p])
            longest_path_seq2.append('-')
        elif direction == 'right':
            q -= 1
            longest_path_seq1.append('-')
            longest_path_seq2.append(seq2[q])
        elif direction == 'diag ':
            p -= 1
            q -= 1
            longest_path_seq1.append(seq1[p])
            longest_path_seq2.append(seq2[q])
        else:
            break

    # Finalize common subsequence
    longest_path_seq1.reverse()
    longest_path_seq2.reverse()
    aligned_sequence1 = ''.join(longest_path_seq1)
    aligned_sequence2 = ''.join(longest_path_seq2)
    max_score = grid[n, m]

    return max_score, aligned_sequence1, aligned_sequence2


