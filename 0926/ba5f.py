def local_alignment(seq1, seq2, n, m):

    # Initialize zero matrix
    path_grid = np.zeros((n+1, m+1), dtype=object)
    path_grid[0, 0] = 'none'

    # Fill row and column
    for i in range(n):
        path_grid[i+1, 0] = 'down '
    for j in range(m):  
        path_grid[0, j+1] = 'right'

    grid = np.zeros((n+1, m+1), dtype=int)

    # Fill 
    # Fill row and column
    for i in range(n):
        grid[i+1, 0] = -(i+1) * 5
    for j in range(m):
        grid[0, j+1] = -(j+1) * 5

    for i in range(n):
        for j in range(m):
            seq1_base = seq1[i]
            seq2_base = seq2[j]
            penalty = pam250[seq1_base][seq2_base]

            x = grid[i, j+1] - 5
            y = grid[i+1, j] - 5
            z = grid[i, j] + penalty
            maxyz = max(x, y, z, 0)
            if maxyz == x:
                path_grid[i+1, j+1] = 'down '
            elif maxyz == y:
                path_grid[i+1, j+1] = 'right'
            elif maxyz == z:
                path_grid[i+1, j+1] = 'diag '
            else:
                path_grid[i+1, j+1] = 'pass '
            grid[i+1, j+1] = maxyz
    
    # print(path_grid)
    # print(grid)

    scores_list = []
    for k in range(n):
        for l in range(m):
            scores_list.append(grid[k+1, l+1])
    max_score = str(max(scores_list))

    # Get the index of the max_score
    max_i, max_j = unravel_index(grid.argmax(), grid.shape)
    seq1 = seq1[:max_i]
    seq2 = seq2[:max_j]

    # Longest path
    p = len(seq1)
    q = len(seq2)
    longest_path_seq1 = []
    longest_path_seq2 = []
    while p >= 0 and q >= 0:
        direction = path_grid[p, q]
        if direction == 'down ':
            p -= 1
            longest_path_seq1.append(seq1[p])
            longest_path_seq2.append('-')
        elif direction == 'right':
            q -= 1
            longest_path_seq1.append('-')
            longest_path_seq2.append(seq2[q])
        elif direction == 'diag ':
            p -= 1
            q -= 1
            longest_path_seq1.append(seq1[p])
            longest_path_seq2.append(seq2[q])
        else:
            break

     # Finalize aligned sequences
    longest_path_seq1.reverse()
    longest_path_seq2.reverse()
    aligned_sequence1 = ''.join(longest_path_seq1)
    aligned_sequence2 = ''.join(longest_path_seq2)

    return max_score, aligned_sequence1, aligned_sequence2


